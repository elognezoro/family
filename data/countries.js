const countries = groups.flatMap((g) =>
  g.countries.map((c) => ({ ...c, region: g.region }))
);

function getCountry(code) {
  return countries.find((c) => c.code === code);
}

function countryName(code) {
  const c = getCountry(code);
  return c ? c.name : code;
}

